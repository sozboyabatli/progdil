function helpme {
  echo -e "YARDIM:"
  echo "    Herhangi bir alanda kullanılmak üzere bir isim ve bir sıfattan oluşan"
  echo "    tamlamalar yaratan uygulamadır."

  echo -e "KULLANIM:"
  echo "    rasgele adet dil "

  echo -e "SEÇENEKLER:"
  echo "    -h ya da --help : yardımı gösterir."
  echo "    adet(rakam) : Kaç adet tamlama üretilsin?"
  echo "    -tr : Türkçe tamlamalar üretir."
  echo "    -en : İngilizce tamlamalar üretir."
  echo "    -d : Dizini kontrol et."

  echo -e "Örnek: 
      'rasgele 2 -tr'"
  echo -e "Çıktı: 
      'zarif efe
      gevrek zeytin'"
  exit
}

function uretec() {
  adet="$1"
  dil="$2"

  if [ "$dil" == "" ]
  then
    dil="-tr"
  fi

  if [ "$adet" == "" ]
  then
    adet="1"
  fi

  if [ "$1" == "-h" ]
  then
    helpme
  fi
  
  sayac=0
  while [ "$sayac" -lt $adet ]
  do
    case $dil in 
      "-en")
        echo ${adjectives[$(( $RANDOM%${#adjectives[@]} ))]}" "${names[$(( $RANDOM%${#names[@]} ))]}
      ;;
      "-tr")
        echo ${sifatlar[$(( $RANDOM%${#sifatlar[@]} ))]}" "${adlar[$(( $RANDOM%${#adlar[@]} ))]}
      ;;    
    esac
    sayac=$(( sayac+1 ))
  done
}

function yazdir {
  cikti=`uretec $1 $2`
  echo -e "$cikti\n"

  if [ "$1" == 1 ] && [ "$3" == "-d" ]
    then
    if [ ! -d "$cikti" ]
      then
      echo "Böyle bir proje dizini yok, oluşturulsun mu? (e/H) "
      read cevap
       if [ "$cevap" == "e" ]
       then
        mkdir "$cikti"
       fi
    fi
  fi
}
yazdir $@

